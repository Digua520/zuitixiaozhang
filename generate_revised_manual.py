def tb(doc, headers, rows):
    table = doc.add_table(rows=1+len(rows), cols=len(headers))
    table.style = 'Light Grid Accent 1'
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = h
        for p in cell.paragraphs:
            for run in p.runs:
                run.bold = True
    for r, row in enumerate(rows):
        for c, val in enumerate(row):
            table.rows[r+1].cells[c].text = str(val)
    doc.add_paragraph()
    return table

def h1(doc, text): return doc.add_heading(text, level=1)
def h2(doc, text): return doc.add_heading(text, level=2)
def h3(doc, text): return doc.add_heading(text, level=3)
def bp(doc, text):
    p = doc.add_paragraph()
    p.add_run(text).bold = True
def ap(doc, text): return doc.add_paragraph(text)
def bl(doc, text): return doc.add_paragraph(text, style='List Bullet')
def divider(doc):
    p = doc.add_paragraph()
    p.add_run('—' * 50)
    ap(doc, '')

