def write_day(doc, d):
    """写入一天的内容"""
    h2(doc, f'Day {d["day"]}：{d["topic"]}')
    bp(doc, f'内容类型：{d["type"]}　|　目标时长：{d["duration"]}')

    # 1. 脚本与话术
    h3(doc, '一、脚本与话术（完整大纲）')
    ap(doc, d['script'].strip())

    # 2. 拍摄注意
    h3(doc, '二、拍摄注意')
    ap(doc, d['filming'])

    # 3. 剪辑要点
    h3(doc, '三、剪辑要点')
    ap(doc, d['editing'])

    # 4. 标题与标签
    h3(doc, '四、标题文案与话题标签')
    bp(doc, '发布标题：')
    ap(doc, d['title'])
    bp(doc, '话题标签（复制使用）：')
    ap(doc, d['hashtags'])

    # 5. 评论与避坑
    h3(doc, '五、评论区引导')
    ap(doc, d['comment_guide'])

    h3(doc, '六、今日避坑提醒')
    ap(doc, d['pitfalls'])

    divider(doc)


